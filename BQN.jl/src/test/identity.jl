function test_identity(only=nothing)
  cases = [
    # Fold
    (1, """ ∧´ {𝕩≡𝕎´⟨⟩}´¨ ⟨+‿0,-‿0,×‿1,÷‿1,∨‿0,∧‿1⟩ """),
    (1, """ ∧´ {𝕩≡𝕎´⟨⟩}´¨ ⟨⋆‿1,¬‿1,⌊‿∞,⌈‿¯∞⟩ """),
    (1, """ ∧´ {𝕩≡𝕎´⟨⟩}´¨ ⟨≠‿0,=‿1,>‿0,≥‿1⟩ """),
    (BQNError, """ √´↕0 """),
    (BQNError, """ |´↕0 """),
    (BQNError, """ ⊢´"" """),
    (BQNError, """ ⊣´"" """),
    (BQNError, """ ∾´⟨⟩ """),
    (BQNError, """ ≍´⟨⟩ """),

    # Insert
    (1, """ ∧´ {(3‿1⥊𝕩)≡𝕎˝0‿3‿1⥊""}´¨ ⟨+‿0,-‿0,×‿1,÷‿1,∨‿0,∧‿1,⋆‿1,¬‿1,⌊‿∞,⌈‿¯∞,≠‿0,=‿1,>‿0,≥‿1⟩ """),
    (BQNError, """ ∾˝"" """),
    (1, """ (∾˝(↕5)⥊"") ≡ (≠⟜1⊸/↕5)⥊"" """),
    (1, """ (∾˝¨≡⥊¨) (≍⥊0˙)⌜˜0‿2‿5 """),
    (BQNError, """ ⌽˝0‿3⥊⟨⟩ """),
  ]
  @testset "identity" begin run_testsuite(cases, only=only) end
  nothing
end
